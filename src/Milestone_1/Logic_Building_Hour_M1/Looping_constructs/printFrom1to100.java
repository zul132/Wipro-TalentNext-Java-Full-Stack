package Milestone_1.Logic_Building_Hour_M1.Looping_constructs;

//Write a program to print all numbers from 1 to 100 i.e. 1 2 3 4 5 6 7 . . . 98 99 100

public class printFrom1to100 {

	public static void main(String[] args) {
		for(int i = 1; i<=100; i++)
		{
			System.out.print(i + " ");
		}
	}

}
